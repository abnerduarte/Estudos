#Tipos de Software: 
#Tipos básicos: programa de apoio à outros programas ex: Sistemas Operacionais; 

#De tempor real: São aqueles que controlam, analisam e mnitoram eventos da vida real ex: detectores de presença automatizados; 

#Comercial: São aqueles de tomadas de decisões comerciais e administrativa; 

#Cientifico e de Engenharia: Algoritimos de processamento de numeros - ex: aqueles que fazem a previsão do tempo; 

#Embutido: São aqueles que controla produtos e sistemas de mercado- ex: aqueles que são embutidos em maquinas de ressonancia magnética; 

#De computador pessoal: São aqueles exitentes para processamento de texto, planilhas eletronicas e etc; 

#De Inteligência artificial: algoritimos não numericos para resolver problemas que não sejam favorável à computação - ex: software pra reconhecimento facial; 

#Mobile: São aplivativos para dispositivos móveis são acoplados a hardwares diferentes e específico